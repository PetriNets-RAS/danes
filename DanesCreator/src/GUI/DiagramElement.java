package GUI;

import Core.Element;

/**
 *
 * @author marek
 */
public class DiagramElement {
    private int x;
    private int y;

    public DiagramElement(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    
    
}
